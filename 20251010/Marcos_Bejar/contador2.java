public class contador2 {
    public static void main(String[] args) {
        // Iniciamos el contador en 2 (primer número par)
        int contador = 2;

        // Bucle que muestra los números pares hasta 200
        while (contador <= 200) {
            System.out.println(contador);
            contador += 2; // Incrementa de 2 en 2
        }
    }
}

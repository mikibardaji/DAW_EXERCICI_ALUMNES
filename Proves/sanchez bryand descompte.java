import java.util.Scanner;

public class descompte {
    public static void main(String[] args) throws Exception {
    Scanner sc = new Scanner(System.in);
        double preu;
        System.out.print("Cuanto cuesta el producto? ");
        preu = sc.nextDouble();

        int descompte;
        System.out.print("Cuanto descuento tiene?");
        descompte = sc.nextInt();

        double resta = (preu * descompte) / 100;
        double total = preu - resta;
        System.out.println("El producto cuesta " + total + " en descuento.");
    }
}
